$SV->Arguments ("-ORBdebuglevel $debug_level ".
                "-ORBSvcConf $server_svcfile -o $server_iorfile");
$CL->Executable ("ami_client");
$CL->Arguments ("-ORBSvcConf $client_svcfile ".
                "-k file://$client_iorfile");

$server_status = $SV->Spawn ();

if ($server_status != 0) {
